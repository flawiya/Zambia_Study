"""
sensitivity_with_quality.py — Crop Area + Pixel Quality Sensitivity Analysis
=============================================================================
Purpose:
  Extends Week_10's sensitivity analysis by adding pixel-overlap quality
  as an additional dimension. This proves that the temporal grouping
  results are robust to both crop area thresholds AND district quality.

Method:
  1. For each crop area threshold (0%, 10%, 25%, 50%):
     a. Run with ALL districts
     b. Run with only High/Medium quality districts
  2. Compare the temporal grouping strength (avg inter-district correlation)
  3. If both tracks converge to the same conclusion → centroid method validated

Outputs:
  - zambia_sensitivity_comparison.csv — full results table
  - zambia_sensitivity_figure.png — 2x2 comparison panel
"""

